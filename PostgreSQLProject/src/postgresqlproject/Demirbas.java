    private void btnGoruntuleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoruntuleActionPerformed
        // TODO add your handling code here:
        
         ArrayList veriler = new ArrayList();
        if (con != null) {

            try {
                String sql = "select * from demirbasguncelle";

                Statement s = con.createStatement();
                ResultSet rs = s.executeQuery(sql);

                while (rs.next()) {

                    veriler.add("Eski Tür : " + rs.getString("eskiTur") + " - " + "Yeni Tür : " + rs.getString("yeniTur"));

                }

                for (int i = 0; i < veriler.size(); i++) {

                    dlm.addElement(veriler.get(i));

                }
            } catch (SQLException ex) {
                Logger.getLogger(TumKullanicilar.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnGoruntuleActionPerformed


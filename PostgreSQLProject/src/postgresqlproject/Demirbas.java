    public void demirbasGoruntule() {

        if (con != null) {

            try {
                String sql = "select \"demirbasId\", tur, kod, adet, dernek.mail as \"mail adresi\" from demirbas inner join dernek on demirbas.dernek = \"dernek\".\"dernekId\";";

                Statement s = con.createStatement();
                ResultSet rs = s.executeQuery(sql);

                while (rs.next()) {

                    dtm.addRow(new Object[]{rs.getInt("demirbasId"), rs.getString("tur"), rs.getInt("kod"), rs.getInt("adet"), rs.getString("mail adresi")});

                }
            } catch (SQLException ex) {
                Logger.getLogger(Demirbas.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }

